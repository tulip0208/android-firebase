package com.codecamp.chatapp.ui.dialog.bottom_dialog;

import androidx.constraintlayout.widget.ConstraintLayout;

public class Theme {
    public ConstraintLayout green;
    public ConstraintLayout orange;
    public ConstraintLayout skyBlue;

    public Theme(ConstraintLayout green, ConstraintLayout orange, ConstraintLayout skyBlue) {
        this.green = green;
        this.orange = orange;
        this.skyBlue = skyBlue;
    }

}
